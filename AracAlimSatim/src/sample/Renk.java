package sample;

public class Renk {
    private int renkID;
    private String renk;

    public Renk() {
    }

    public Renk(int renkID, String renk) {
        this.renkID = renkID;
        this.renk = renk;
    }

    public int getRenkID() {
        return renkID;
    }

    public void setRenkID(int renkID) {
        this.renkID = renkID;
    }

    public String getRenk() {
        return renk;
    }

    public void setRenk(String renk) {
        this.renk = renk;
    }

}
